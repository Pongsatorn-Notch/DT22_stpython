    if from_currency in rates and to_currency in rates:
        
        # ‡πÅ‡∏õ‡∏•‡∏á‡∏à‡∏≤‡∏Å‡∏™‡∏Å‡∏∏‡∏•‡πÄ‡∏á‡∏¥‡∏ô‡∏ï‡∏±‡πâ‡∏á‡∏ï‡πâ‡∏ô‡πÄ‡∏õ‡πá‡∏ô USD ‡∏Å‡πà‡∏≠‡∏ô
        rate_from_usd = 1 / rates[from_currency] # ‡∏™‡∏°‡∏°‡∏ï‡∏¥‡∏ê‡∏≤‡∏ô‡∏ß‡πà‡∏≤ API ‡πÄ‡∏õ‡πá‡∏ô‡πÅ‡∏ö‡∏ö USD-base
        
